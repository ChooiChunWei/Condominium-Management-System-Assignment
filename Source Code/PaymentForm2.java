    public PaymentForm2() {
        try {
            initComponents();
            
            //Set value to text box 
            UnitNoTxt.setText(UnitNo);
            
            //Declare variables
            PreparedStatement ps,ps1;
            //Declare variable for the current date
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date CurrentDate = new Date();
                        
            //SQL Select query to get Outstanding Balance,Due Date and Resident ID from Unit table
            ps = con.prepareStatement("SELECT `Outstanding_Balance`, `Due_Date`,`Resident_ID` FROM `unit` WHERE `Unit_ID`=?");
            ps.setString(1,UnitNo);
            ResultSet result = ps.executeQuery();
            
            result.next();
            OutstandingBalance = result.getInt("Outstanding_Balance");
            Date DueDate = result.getDate("Due_Date");
            String ResidentID = result.getString("Resident_ID");
            
            
            //SQL Select query to get Outstanding Balance and Due Date from Unit table
            ps1 = con.prepareStatement("SELECT `Resident_Name` FROM `resident` WHERE `Resident_ID` = ?");
            ps1.setString(1,ResidentID);
            ResultSet result1 = ps1.executeQuery();
            
            result1.next();
            ResidentName = result1.getString("Resident_Name");
            
            
            //Set values to the text box
            //Username
            UsernameTxt.setText(ResidentName);
            //Outstanding Balance
            OutstandingBalanceTxt.setText(Integer.toString(OutstandingBalance));
            
            
            //Fine
            //Current Date is after Due Date
            if(dateFormat.format(CurrentDate).compareTo(dateFormat.format(DueDate))>0){
                FineTxt.setText("50");
            //Current Date is before Due Date    
            }else if(dateFormat.format(CurrentDate).compareTo(dateFormat.format(DueDate))<0){
                FineTxt.setText("0");
            //Current date is equal to Due Date
            }else if(dateFormat.format(CurrentDate).compareTo(dateFormat.format(DueDate)) == 0){
                FineTxt.setText("0");
            }
            
            //Total
            Fine = Integer.parseInt(FineTxt.getText());
            total = OutstandingBalance + Fine;
            TotalTxt.setText(Integer.toString(total));
            
            
        } catch (SQLException ex) {
            Logger.getLogger(PaymentForm2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

