    private void PayButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PayButActionPerformed
        try {
            PreparedStatement ps,ps1,ps2,ps3;
            //Declare variable for the current date
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date CurrentDate = new Date();
            //Declare variable to check the progress of the code
            int check;
            
            //SQL Select query to get Due Date and Resident ID from Unit table
            ps = con.prepareStatement("SELECT `Due_Date`,`Resident_ID` FROM `unit` WHERE `Unit_ID`=?");
            ps.setString(1,UnitNo);
            ResultSet result = ps.executeQuery();
            
            result.next();
            Date DueDate = result.getDate("Due_Date");
            String ResidentID = result.getString("Resident_ID");
            check = 1;
            
            if(check == 1){
            //SQL INSERT Query to insert payment's info into payment table
            ps1 = con.prepareStatement("INSERT INTO `payment`(`Payment_Date`, `Due_Date`, `Payment_Amount`, `Resident_ID`) VALUES (?,?,?,?)");
            ps1.setString(1,dateFormat.format(CurrentDate));
            ps1.setString(2,dateFormat.format(DueDate));
            ps1.setString(3,TotalTxt.getText());
            ps1.setString(4,ResidentID);
            ps1.executeUpdate();
            check = 2;
            }
            
            if(check == 2){
            //SQL UPDATE Query to reset the Outstanding Balance and Due Date in unit table
            ps2 = con.prepareStatement("UPDATE `unit` SET `Outstanding_Balance`= NULL, `Due_Date`= NULL WHERE `Unit_ID` = ?");
            ps2.setString(1,UnitNo); 
            ps2.executeUpdate();
            check = 3;
            }
            
            if(check == 3){
            //SQL UPDATE Query to set the account status to active in resident table 
            ps3 = con.prepareStatement("UPDATE `resident` SET `Account_Status`= \"Active\" WHERE `Resident_ID` = ?");
            ps3.setString(1,ResidentID); 
            ps3.executeUpdate();
            check = 4;
            }
            
            if(check == 4){
            JOptionPane.showMessageDialog(this,"Payment Done Successfully!","Payment Message",JOptionPane.INFORMATION_MESSAGE);
            
            //Open Receipt Form
            //Pass the value to the UnitNoFromPF2
            UnitNoFromPF2 = UnitNo;
            ReceiptForm rf = new ReceiptForm();
            rf.setVisible(true);
            rf.pack();
            rf.setLocationRelativeTo(null);
            rf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.setVisible(false);
        
            }else{
                JOptionPane.showMessageDialog(this,"Payment Failed!","Payment Message",JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(PaymentForm2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_PayButActionPerformed

